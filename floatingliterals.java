class floatingliterals{
    public static void main(String[] args){
        float a=101.230f; //decimal form literal
        float b=0123.222f;
        double c=0X1.8p3;
        
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        char str='g';
        System.out.println(str);
        String str1="gkjfnrj";
        System.out.println(str1);
        
    }
}